/**
 * HashMap Identification
 *
 * - Stores data as key-value pairs.
 * - Keys are unique; values can be duplicated.
 * - Average-case time complexity for CRUD operations (Create, Read, Update, Delete) is O(1).
 * - Worst-case time complexity is O(n), due to hash collisions.
 *
 * When to Use:
 * - When you need **fast lookups** or **associative access** by a key.
 * - For problems involving **frequency counting** (e.g. character counts, word counts).
 * - For solving **Two Sum**, **Subarray Sum Equals K**, or **detecting duplicates**.
 * - Implementing **caches** (e.g. LRU Cache using HashMap + LinkedList).
 * - When **mapping relationships** (e.g. ID to name, email to user info).
 */

